    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Set Reset button enabled
        jButton1.setEnabled(true);
        //Convert
        
        float result;
        if(entree.getText().matches("\\d+\\.\\d+")){
        result = Float.parseFloat(entree.getText());
        
        switch (currency.getSelectedIndex()){
            case 0:output.setText((result*114.64)+"");
                    status.setText("To DZD!");
                break;
            case 1:output.setText((result*1.06)+"");
                    status.setText("To USD!");
                break;
            case 2:output.setText((result*0.7)+"");
                      status.setText("To GBP!");
                break;
               
        }
        }
        else
            status.setText("Error !");
    }//GEN-LAST:event_jButton2ActionPerformed

